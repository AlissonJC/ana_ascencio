radius = float(input("Digite o valor do raio: "))

pi = 3.1415
length_sphere = 2 * pi * radius
area_sphere = pi * radius**2
volume_sphere = 3/4 * pi * radius**3

print(f"O valor do comprimento da esfera é de: {length_sphere:.2f}")
print(f"O valor da área da esfera é de: {area_sphere:.2f}")
print(f"O valor do volume da esfera é de: {volume_sphere:.2f}")